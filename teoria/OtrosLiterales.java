	public class OtrosLiterales {
	  public static void main(String[] args){
	    //tipo float
	    float variableFloat = 123;
	    System.out.println("Valor de la variable float: " +
				variableFloat);
	    float otraVariableFloat = 12.3f; //la f necesaria
	    System.out.println("Valor de la variable float: " +
				otraVariableFloat);
	    float otraNuevaVariableFloat = 2e5f; //la f necesaria
	    System.out.println("Valor de la variable float: " +
				otraNuevaVariableFloat);
            //tipo double
	    double variableDouble = 5123;	
	    double otraVariableDouble = 5123l;	
	    double otraNuevaVariableDouble = 5123.23f;	
	    System.out.println("Valor de variables double: " +
				variableDouble + "--" + 
				otraVariableDouble + "--" +
				otraNuevaVariableDouble);
	    double varDouble = -23e-23;
	    System.out.println("Valor de la variable double: " + 
				varDouble);
		
	
	
	  }
	}
